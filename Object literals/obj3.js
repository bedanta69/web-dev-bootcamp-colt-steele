//adding object in object literals 
let objectliteral = {
    sport: "basketball",
    player: "kobe Bryant"
};

objectliteral.nickname = "Mamba" // adds the object nickname to objectliteral
console.log(objectliteral);

//updating object in objectliteral
let objectliteral2 = {
    title: "Goat",
    player: "Kobe Bryant"
};

objectliteral2.player = "Michael Jordan";
console.log(objectliteral2); //updates the value of player from Kobe Bryant to Michael Jordan 